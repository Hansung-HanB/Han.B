package com.example.hanb;

public class ProgramRecommendItem {
    public String recommendProgram, programGrade;

    public void setRecommendProgram(String program) {
        recommendProgram = program;
    }

    public void setProgramGrade(String grade) {
        programGrade = grade;
    }

    public String getRecommendProgram() {
        return this.recommendProgram;
    }

    public String getProgramGrade() {
        return this.programGrade;
    }
}